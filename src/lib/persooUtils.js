var docCookies = {
  getItem: function (sKey) {
    if (!sKey) { return null; }
    return decodeURIComponent(document.cookie.replace(new RegExp("(?:(?:^|.*;)\\s*" + encodeURIComponent(sKey).replace(/[\-\.\+\*]/g, "\\$&") + "\\s*\\=\\s*([^;]*).*$)|^.*$"), "$1")) || null;
  },
  setItem: function (sKey, sValue, vEnd, sPath, sDomain, bSecure) {
    if (!sKey || /^(?:expires|max\-age|path|domain|secure)$/i.test(sKey)) { return false; }
    var sExpires = "";
    if (vEnd) {
      switch (vEnd.constructor) {
        case Number:
          sExpires = vEnd === Infinity ? "; expires=Fri, 31 Dec 9999 23:59:59 GMT" : "; max-age=" + vEnd;
          break;
        case String:
          sExpires = "; expires=" + vEnd;
          break;
        case Date:
          sExpires = "; expires=" + vEnd.toUTCString();
          break;
      }
    }
    document.cookie = encodeURIComponent(sKey) + "=" + encodeURIComponent(sValue) + sExpires + (sDomain ? "; domain=" + sDomain : "") + (sPath ? "; path=" + sPath : "") + (bSecure ? "; secure" : "");
    return true;
  },
  removeItem: function (sKey, sPath, sDomain) {
    if (!this.hasItem(sKey)) { return false; }
    document.cookie = encodeURIComponent(sKey) + "=; expires=Thu, 01 Jan 1970 00:00:00 GMT" + (sDomain ? "; domain=" + sDomain : "") + (sPath ? "; path=" + sPath : "");
    return true;
  },
  hasItem: function (sKey) {
    if (!sKey) { return false; }
    return (new RegExp("(?:^|;\\s*)" + encodeURIComponent(sKey).replace(/[\-\.\+\*]/g, "\\$&") + "\\s*\\=")).test(document.cookie);
  },
  keys: function () {
    var aKeys = document.cookie.replace(/((?:^|\s*;)[^\=]+)(?=;|$)|^\s*|\s*(?:\=[^;]*)?(?:\1|$)/g, "").split(/\s*(?:\=[^;]*)?;\s*/);
    for (var nLen = aKeys.length, nIdx = 0; nIdx < nLen; nIdx++) { aKeys[nIdx] = decodeURIComponent(aKeys[nIdx]); }
    return aKeys;
  }
};


/**
 * Simple support for basket status
 */
var basketCookieName = "persooBasketItems";

function getBasketItems() {
	if (docCookies.hasItem(basketCookieName)) {
		return JSON.parse( docCookies.getItem(basketCookieName) );
	} else {
		return null;
	}
}

function addToBasketInCookies(itemID, price) {
	var basketItems = getBasketItems() || {};
	if (basketItems[itemID]) {
		basketItems[itemID].quantity++;
	} else {
		basketItems[itemID] = {price: price, quantity: 1};
	}
	docCookies.setItem(basketCookieName, JSON.stringify(basketItems));
}

function deleteBasketInCookies() {
	docCookies.removeItem(basketCookieName);
}

function sumBasketItems(basketItems) {
	var total = 0;
	for (var itemID in basketItems) {
		total += basketItems[itemID].price * basketItems[itemID].quantity;
	}
	return total;
}

function purchaseBasketInCookies() {
	var basketItems = getBasketItems();
	if (basketItems) {
		persoo('send', 'pageview', {
			transaction: { 
				id: 1*new Date(),
                affiliation: "universal persoo eshop",
                revenue: sumBasketItems(basketItems),
                shipping: 0,
                tax: 0			
			},
			basketItems: basketItems,
		    basketTotal: sumBasketItems(basketItems)
	    })
	}
	deleteBasketInCookies();
}